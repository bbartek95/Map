//creating first icon
var DundeeIcon = L.icon({
	iconUrl: 'UoDLogo.svg',
	iconSize: [20, 20],
	iconAnchor: [10, 10],
	popupAnchor: [0, -10],
});

//creating second icon
var DundeeGrey = L.icon({
	iconUrl: 'logo-grey.svg',
	iconSize: [20, 20],
	iconAnchor: [10, 10],
	popupAnchor: [0, -10],
});

var mapLayerGroups = [];

//displaying the GCRF projects data on the map
var projects = L.geoJSON(project, {
	pointToLayer : function(geoJsonPoint, geometry) {
		var marker = L.marker(geometry, {icon : DundeeIcon});
		return marker;
		},
	onEachFeature: function(feature, layer) {
		layer.bindPopup('<b>Partner: </b>' + feature.properties.Organisation + '<br/>' + '<b>PI: </b>' + feature.properties.PI + '<br/>' + '<b>Title: </b>' + feature.properties.Title + '<br/>' + '<b>School: </b>' + feature.properties.School);
	}
	}
);

var ScienceEngineerings = L.geoJson(project, {
  filter : function(feature) {return feature.properties.School == "Science and Engineering"}
});

var Humanities = L.geoJson(project, {
  filter : function(feature) {return feature.properties.School == "Humanities"}
});

var LifeSciences = L.geoJson(project, {
  filter : function(feature) {return feature.properties.School == "Life Sciences"}
});

var allSchools = L.layerGroup([ScienceEngineerings, Humanities, LifeSciences]);

L.control.layers({
  "All schools": allSchools,
  "Science and Engineering" : ScienceEngineerings,
  "Humanities" : Humanities,
  "Life Sciences" : LifeSciences
}).addTo(map);

//displaying the EPDU data on the map
var education = L.geoJSON (EPDU,{
	pointToLayer : function (geoJsonPoint, geometry){
		var marker = L.marker(geometry, {icon : DundeeGrey});
		return marker;
	},
	onEachFeature: function(feature, layer) {
		layer.bindPopup('<b>Partner: </b>' + feature.properties.Organisation + '<br/>' + '<b> Type: </b>' + feature.properties.Type + '<br/>' + '<b>School: </b>' + feature.properties.School);
	}
	}
);

//this makes sure that all the markers fit on the map
map.fitBounds(projects.getBounds());


//This creates data clusters
var clusters = L.markerClusterGroup({ disableClusteringAtZoom : 8,
	removeOutsideVisibleBounds : false,
	showCoverageOnHover : false
});
//adds layer to the clusters for the projects and the EPDU data
clusters.addLayer(education);
clusters.addLayer(projects);
//adds the cluster layer to the map
map.addLayer(clusters);

